import java.util.*;
class mian{
  public static void main(String[] args){
    Scanner n=new Scanner(System.in);
    int d=n.nextInt();
    int c=0;
    for(int i=1;i<d;i++){
      if(d%i==0)
      c=c+i;
    }
    if(c>d)
    System.out.print("Abundant Number");
    else if(c<d)
    System.out.print("Deficient Number");
    else
    System.out.print("Perfect Number");
  }
}
